"""
Legion Health Sponsorship Case — Excel Workbook Builder
==========================================================

Builds model/legion_sponsorship_model.xlsx, a founder-editable companion to
model.py/scenarios.py/sensitivity.py. All calculation cells use LIVE EXCEL
FORMULAS that reference 01_Assumptions (no hardcoded calculated values), so
a founder can edit any Low/Base/High cell on 01_Assumptions and see every
downstream tab recalculate.

Scope note: the live formula chain in 02-05 references the BASE (column D)
value of each assumption — this is the standard "operating case" financial-
modeling convention and is what "founder edits an assumption and sees
results update" means in practice. Low/High columns remain visible and
editable for audit/context, and are explicitly pulled into the 05_Sensitivity
tornado table (which by construction flexes one assumption's Low/High while
holding all others at Base — the same "hold everything else at base" logic
used in sensitivity.py). Full low/base/high x pathway x model scenario grids
(45 rows) and the full 13-assumption x 15-combo tornado (195 rows) are
generated by scenarios.py/sensitivity.py into outputs.csv/sensitivity_outputs.csv;
this workbook is not a re-implementation of that full grid (that would
require a scenario-selector/macro layer disproportionate to a model shell),
it is the auditable, editable base-case companion, plus one illustrative
tornado (ai_driven pathway x Model D, the base case's only economically
positive combo) built with genuine flexed-value formulas.

Run: python3 build_excel.py
Then: python3 <xlsx-skill>/scripts/recalc.py legion_sponsorship_model.xlsx
"""

