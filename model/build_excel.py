def ns_formula_ai_driven_D(override_aid=None, override_col="D"):
    """
    Build the north-star formula for pathway=ai_driven, model=D, with one
    assumption's column swapped to override_col (C=Low or E=High); all
    others held at Base (D). Mirrors sensitivity.py's
    run_base_case_for_pathway_model(pathway='ai_driven', model_key='D...'),
    INCLUDING the core-book contagion term (A25 x A26/100, NEW v2) --
    fixed (2026-07-08) to include this term, which the illustrative
    tornado formula had omitted since the v2 core-book-contagion term was
    added (a pre-existing gap vs. sensitivity.py, not a Phase-5-introduced
    change; it does not affect the swing values for the assumptions
    actually flexed here, since A25/A26 are not among TORNADO_AIDS below,
    but it DID understate every North-Star @ Low/High cell by a constant
    ~$4.06/patient/yr at the new base case).
    """
    def c(aid):
        return override_col if aid == override_aid else "D"

    care_cost = f"({A('A02b', c('A02b'))}*{A('A04', c('A04'))})"
    servicing = f"(0.5*({A('A08', c('A08'))}*{A('A23', c('A23'))})/{A('A13', c('A13'))})"
    commercial = f"(0.5*({A('A08', c('A08'))}*{A('A23', c('A23'))})/{A('A13', c('A13'))})"
    compliance = f"({A('A09', c('A09'))}/{A('A13', c('A13'))})"
    pmpm = f"({A('A15', c('A15'))}/12)"
    util = f"({A('A21', c('A21'))}/100)"
    arpu_d = f"({pmpm}*12*{util})"
    trust = f"({arpu_d}*({A('A10', c('A10'))}+{A('A11', c('A11'))})/100)"
    contagion = f"({A('A25', c('A25'))}*{A('A26', c('A26'))}/100)"
    return f"={arpu_d}-{care_cost}-{servicing}-{commercial}-{compliance}-{trust}-{contagion}"


